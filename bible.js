function isCacheValid(day) {
  const cachedDay = parseInt(localStorage.getItem(CACHE_PLAN_DAY_KEY), 10);
  return cachedDay === day;
}

function saveToCache(data, day) {
  localStorage.setItem(CACHE_KEY, JSON.stringify(data));
  localStorage.setItem(CACHE_PLAN_DAY_KEY, day.toString());
}

function getFromCache() {
  const data = localStorage.getItem(CACHE_KEY);
  return data ? JSON.parse(data) : null;
}

async function fetchPassageText(reference) {
  const url = `https://api.scripture.api.bible/v1/bibles/${BIBLE_ID}/passages/${reference}?content-type=html&include-titles=true&include-chapter-numbers=false&include-verse-numbers=true`;

    console.log(reference);

  const response = await fetch(url, {
    headers: { 'api-key': apiKey }
  });

  if (!response.ok) throw new Error(`Failed to fetch ${reference}`);
  const data = await response.json();
  return data.data.content || "(No content)";
}

async function loadTodaysReadings() {
  const now = new Date();
  const day = getPlanDayNumber(PLAN_START_DATE, now);
  const readings = classicPlan[day];

  if (!readings) {
    otElement.innerText = 'No reading found.';
    ntElement.innerText = 'No reading found.';
    wisElement.innerText = 'No reading found.';
    return;
  }

  if (isCacheValid(day)) {
    const cached = getFromCache();
    otElement.innerHTML = makeHeading(readings.ot) + cached.ot;
    ntElement.innerHTML = makeHeading(readings.nt) + cached.nt;
    wisElement.innerHTML = makeHeading(readings.wis) + cached.wis;
    console.log('Loaded readings from cache.');
    return;
  }

  try {
    otElement.innerText = 'Loading...';
    ntElement.innerText = 'Loading...';
    wisElement.innerText = 'Loading...';

    const [ot, nt, wis] = await Promise.all([
      fetchPassageText(readings.ot),
      fetchPassageText(readings.nt),
      fetchPassageText(readings.wis)
    ]);

    otElement.innerHTML = makeHeading(readings.ot) + ot;
    ntElement.innerHTML = makeHeading(readings.nt) + nt;
    wisElement.innerHTML = makeHeading(readings.wis) + wis;

    saveToCache({ ot, nt, wis }, day);
    console.log('Readings fetched and cached.');
  } catch (err) {
    console.error(err);
    otElement.innerText = 'Error loading OT';
    ntElement.innerText = 'Error loading NT';
    wisElement.innerText = 'Error loading Wisdom';
  }
}

function getBookAbbreviation(reference) {
    const match = reference.match(/^(\d?[A-Z]{2,3})/i);
    console.log(match);
    return match ? match[0] : '';
}

function getbookName(abbreviation) {
    console.log(bookNames[abbreviation.toUpperCase()] || abbreviation);
    return bookNames[abbreviation.toUpperCase()] || abbreviation;
}

function getPassageRange(reference) {
    const match = reference.match(/^(\d?[A-Z]{2,3})\.(\d+)\.(\d+)(?:-(\d?[A-Z]{2,3})?\.(\d+)\.(\d+)|-(\d+))?/);
    if (!match) return '';

    const [
        ,
        startBook, startChapter, startVerse,
        endBook, endChapter, endVerse,
        shortEndVerse
    ] = match;

    console.log(startChapter, startVerse, endChapter, endVerse, shortEndVerse);

    if (endChapter && endVerse) {
        if (startBook === endBook && startChapter === endChapter) {
            // Same book and chapter: drop second chapter
            return `${startChapter}:${startVerse}–${endVerse}`;
        } else {
            return `${startChapter}:${startVerse}–${endChapter}:${endVerse}`;
        }
    } else if (shortEndVerse) {
        return `${startChapter}:${startVerse}–${shortEndVerse}`;
    } else {
        return `${startChapter}:${startVerse}`;
    }
}

function makeHeading(reference) {
    const abbreviation = getBookAbbreviation(reference);
    return `<h3 class=no-margin-no-padding">${getbookName(abbreviation)} ${getPassageRange(reference)}</h3>`;
}

function showPage(index) {
  if (index < 0 || index >= pages.length) return;

  const current = pages[currentPage];
  const next = pages[index];

  if (current === next) return;

  // Fade out current
  current.classList.remove('active');
  setTimeout(() => {
    current.style.display = 'none';
    next.style.display = 'block';

    // Force reflow to restart animation
    void next.offsetWidth;

    next.classList.add('active');
    currentPage = index;
  }, 300); // matches your transition time

  document.getElementById('reading-wrapper').scrollTo({
    top: 0,
    behavior: 'smooth'
  });
}

